export const ColorTheme = ( props: any ) => {

    if( props === 0 ){

        return '#d34436'
    }
    else if( props === 1 ){

        return '#ffffff'
    }    
    else if( props === 2 ){

        return '#343d42'
    } 
    else if( props === 3 ){

        return '#f0c3c2'
    }
    else if( props === 4 ){

        return '#a5a5a5'
    }
    else if( props === 5 ){

        return '#000000'
    }
    else{
        return '#FFFFFF'
    }
}
